- (void)viewDidLoad
{
    [super viewDidLoad];
    [self setTitle:@"Stores"];
    
}

- (void) viewDidAppear:(BOOL)animated {
    _user = [PMUser sharedInstance];
    [[self tableView] reloadData];
    
    
}

